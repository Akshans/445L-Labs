/*BUTTONS
0 = click
1 = up
2 = down*/

void menuAct(uint32_t button){ 
	/*MENUS
	0 = start screen
	1 = set mode
	2 = play game
	3 = high score
	*/
	if(getMenu() == 0){ //START SCREEN~~~~~~~~
		titleScreen();
		//button = 1000;
		if(button == 0){
			setMenu(1);
			menuInit();
		}
	}
//DELETED ALARM CLOCK MENU = 8
	else if(getMenu() == 1){ //GAME MODE~~~~~~~~~~~~~~~
		//1) TIME MODE
		//2) PRECISION MODE
		gameMode();
		
		if(button == 1){	//UP
			//prolly could make it a switch case but its 4am rn
			if(cursor==1 || cursor == 0){
				if(timeMode == 0 && precisionMode == 0){
					cursor = 2;
				}
				else{
					cursor = 4;
				}
			}
			else if(cursor == 2){
				cursor = 1;
			}
			else if(cursor == 4){
				cursor = 2;
			}
		}	
		if(button == 2){	//DOWN
			
			if(cursor == 0){
				cursor = 1;
			}
			else if(cursor==1){
				cursor = 2;
			}
			else if(cursor == 2){
				if(timeMode == 0 && precisionMode == 0){
					cursor = 1;
				}
				else{
					cursor = 4;
				}
			}
			else if(cursor == 4){
				cursor = 1;
			}
		}		
		else if(button == 0){	//SELECT
			if(cursor==1){
				setTimeMode();
			}
			else if(cursor == 2){
				setPrecisionMode();
			}
			else if(cursor == 4){
				setMenu(2);
				menuInit();
			}
		}
	}
	else if(getMenu() == 2){ //PLAY GAME
		SpriteInit();
		gameFlag = 1;
		
		
	}
	
	else if(getMenu() == 3){ //GAME OVER
		gameOverScreen();
		if(button == 0){
			setMenu(1);
			menuInit();
			menuAct(-1);	//?
		}
	}
}


